public class Operator1 {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;
        int num3 = 10;

        System.out.println(num1 > num2);
        System.out.println(num1 >= num3);
        System.out.println(num1 < num2);
        System.out.println(num1 <= num3);
        System.out.println(num1 == num3);
        System.out.println(num1 != num2);


    }

}
